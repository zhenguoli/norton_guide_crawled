<B>Function 19h            Get Default Drive</B>

    Returns the number of the default drive.

        <B>Entry</B>   AH = 19h

        <B>Return</B>  AL = Drive number (0 = A, 1 = B, 2 = C etc.)

    ------------------------------------------------------------------

    The drive number used by Set/Get Default Drive (functions 0Eh and
    19h) is one less than the drive number used by other functions.
